namespace OpenSky.API.DbModel
{
    using System.ComponentModel.DataAnnotations;

    /// -------------------------------------------------------------------------------------------------
    /// <summary>
    /// Statistic model.
    /// </summary>
    /// <remarks>
    /// sushi.at, 29/01/2022.
    /// </remarks>
    /// -------------------------------------------------------------------------------------------------
    public class Statistic
    {
        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the key.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        [Required]
        [Key]
        public string Key { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the value.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public double Value { get; set; }
    }
}
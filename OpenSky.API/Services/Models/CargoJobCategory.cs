namespace OpenSky.API.Services.Models
{
    /// -------------------------------------------------------------------------------------------------
    /// <summary>
    /// Cargo job category model.
    /// </summary>
    /// <remarks>
    /// sushi.at, 12/12/2021.
    /// </remarks>
    /// -------------------------------------------------------------------------------------------------
    public class CargoJobCategory
    {
        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the maximum airport size.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public int MaxAirportSize { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the maximum distance.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public int MaxDistance { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the maximum hours to expiry.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public int MaxHoursToExpiry { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the minimum airport size.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public int MinAirportSize { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the minimum distance.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public int MinDistance { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the minimum hours to expiry.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public int MinHoursToExpiry { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the payload.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public int Payload { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the Skybucks per nautical mile.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public double SkybucksPerNauticalMile { get; set; }
    }
}
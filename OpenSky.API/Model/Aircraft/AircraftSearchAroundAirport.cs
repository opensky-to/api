namespace OpenSky.API.Model.Aircraft
{
    using OpenSky.API.DbModel.Enums;

    /// -------------------------------------------------------------------------------------------------
    /// <summary>
    /// Aircraft search around airport model.
    /// </summary>
    /// <remarks>
    /// sushi.at, 24/02/2022.
    /// </remarks>
    /// -------------------------------------------------------------------------------------------------
    public class AircraftSearchAroundAirport
    {
        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the airport icao.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public string AirportICAO { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the category.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public AircraftTypeCategory Category { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets a value indicating whether to filter by category (nullable enum for Category was ignored).
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public bool FilterByCategory { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the manufacturer.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public string Manufacturer { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the maximum results.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public int MaxResults { get; set; } = 100;

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the type name.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public string Name { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets a value indicating whether to only include vanilla aircraft in the search results.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public bool OnlyVanilla { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the radius in nautical miles.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public int Radius { get; set; }
    }
}
namespace OpenSky.API.Model
{
    using System.Collections.Generic;

    /// -------------------------------------------------------------------------------------------------
    /// <summary>
    /// World population overview model.
    /// </summary>
    /// <remarks>
    /// sushi.at, 02/07/2021.
    /// </remarks>
    /// -------------------------------------------------------------------------------------------------
    public class WorldPopulationOverview
    {
        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the total number of airports.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public int TotalAirports { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets a list grouping airports by size.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public IEnumerable<AirportSize> AirportSizes { get; set; }
    }

    /// -------------------------------------------------------------------------------------------------
    /// <summary>
    /// Airport counts by size.
    /// </summary>
    /// <remarks>
    /// sushi.at, 02/07/2021.
    /// </remarks>
    /// -------------------------------------------------------------------------------------------------
    public class AirportSize
    {
        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the size.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public string Size { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the number of airports.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public int Airports { get; set; }
    }
}
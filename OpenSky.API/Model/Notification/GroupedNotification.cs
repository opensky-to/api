namespace OpenSky.API.Model.Notification
{
    using System;
    using System.Collections.Generic;

    using OpenSky.API.DbModel.Enums;

    /// -------------------------------------------------------------------------------------------------
    /// <summary>
    /// Grouped notification model.
    /// </summary>
    /// <remarks>
    /// sushi.at, 19/12/2023.
    /// </remarks>
    /// -------------------------------------------------------------------------------------------------
    public class GroupedNotification
    {
        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the display timeout.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public int? DisplayTimeout { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the Date/Time of the email fallback.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public DateTime? EmailFallback { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the Date/Time of the expiration.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public DateTime? Expires { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the identifier of the grouping.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public Guid GroupingID { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the Date/Time of the marked for deletion time.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public DateTime? MarkedForDeletion { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the message.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public string Message { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the recipients.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public List<GroupedNotificationRecipient> Recipients { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the sender.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public string Sender { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the style.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public NotificationStyle Style { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the target.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public NotificationTarget Target { get; set; }
    }
}